// renderIndex generates the INDEX.md content.
func renderIndex(packages []*PackageInfo) string {
	var sb strings.Builder
	sb.WriteString("---\n")
	sb.WriteString("type: auto-generated-index\n")
	sb.WriteString(fmt.Sprintf("generated_at: %s\n", time.Now().UTC().Format(time.RFC3339)))
	sb.WriteString(fmt.Sprintf("package_count: %d\n", len(packages)))
	sb.WriteString("---\n\n")
	sb.WriteString("# Package Index\n\n")
	sb.WriteString("> Auto-generated by `cmd/docgen`. Do not edit manually.\n")
	sb.WriteString("> Regenerate with `make docs`.\n\n")

	// Group by layer.
	byLayer := make(map[string][]*PackageInfo)
	for _, pkg := range packages {
		byLayer[pkg.Layer] = append(byLayer[pkg.Layer], pkg)
	}

	sb.WriteString("| Package | Layer | Key Exports | Context |\n")
	sb.WriteString("|---------|-------|-------------|----------|\n")

	for _, layer := range layers {
		pkgs := byLayer[layer.Name]
		if len(pkgs) == 0 {
			continue
		}
		for _, pkg := range pkgs {
			exports := summarizeExports(pkg)
			sb.WriteString(fmt.Sprintf("| `%s` | %s | %s | [CONTEXT](../../%s/CONTEXT.md) |\n",
				pkg.ImportPath, layer.Label, exports, pkg.ImportPath))
		}
	}

	sb.WriteString(fmt.Sprintf("\n---\n*%d packages across %d layers.*\n",
		len(packages), countLayers(byLayer)))

	return sb.String()
}

// renderPackage generates a per-package CONTEXT.md.
func renderPackage(pkg *PackageInfo, deps *DependencyGraph, impact *PackageImpact) string {
	var sb strings.Builder

	// Frontmatter.
	sb.WriteString("---\n")
	sb.WriteString(fmt.Sprintf("package: %s\n", pkg.Name))
	sb.WriteString(fmt.Sprintf("import_path: %s\n", pkg.ImportPath))
	sb.WriteString(fmt.Sprintf("layer: %s\n", pkg.Layer))
	sb.WriteString(fmt.Sprintf("generated_at: %s\n", time.Now().UTC().Format(time.RFC3339)))
	sb.WriteString(fmt.Sprintf("source_files: [%s]\n", strings.Join(pkg.SourceFiles, ", ")))
	sb.WriteString("---\n\n")
	sb.WriteString(fmt.Sprintf("# %s\n\n", pkg.ImportPath))
	sb.WriteString(fmt.Sprintf("> Layer: **%s** · Files: %d · ",
		layerLabel(pkg.Layer), len(pkg.SourceFiles)))
	sb.WriteString(fmt.Sprintf("Interfaces: %d · Structs: %d · Functions: %d\n\n",
		len(pkg.Interfaces), len(pkg.Structs), len(pkg.Functions)))

	// Interfaces section.
	if len(pkg.Interfaces) > 0 {
		sb.WriteString("## Interfaces\n\n")
		for _, iface := range pkg.Interfaces {
			sb.WriteString(fmt.Sprintf("### %s", iface.Name))
			if len(iface.Methods) > 0 {
				sb.WriteString(fmt.Sprintf(" (%d methods)", len(iface.Methods)))
			}
			sb.WriteString("\n")
			if iface.Doc != "" {
				sb.WriteString(fmt.Sprintf("> %s\n", iface.Doc))
			}
			sb.WriteString("\n```go\ntype " + iface.Name + " interface {\n")
			for _, m := range iface.Methods {
				sb.WriteString(fmt.Sprintf("    %s%s\n", m.Name, m.Signature))
			}
			sb.WriteString("}\n```\n\n")
		}
	}

	// Structs section.
	if len(pkg.Structs) > 0 {
		sb.WriteString("## Structs\n\n")
		for _, st := range pkg.Structs {
			line := fmt.Sprintf("- **%s**", st.Name)
			if st.FieldCount > 0 {
				line += fmt.Sprintf(" — %d fields", st.FieldCount)
			}
			if len(st.FieldNames) > 0 {
				names := st.FieldNames
				if len(names) > 8 {
					names = append(names[:8], "...")
				}
				line += fmt.Sprintf(": %s", strings.Join(names, ", "))
			}
			if len(st.Embeds) > 0 {
				line += fmt.Sprintf(" (embeds %s)", strings.Join(st.Embeds, ", "))
			}
			sb.WriteString(line + "\n")
		}
		sb.WriteString("\n")
	}

	// Function types.
	if len(pkg.FuncTypes) > 0 {
		sb.WriteString("## Function Types\n\n")
		for _, ft := range pkg.FuncTypes {
			sb.WriteString(fmt.Sprintf("- `%s` — `func%s`\n", ft.Name, ft.Signature))
		}
		sb.WriteString("\n")
	}

	// Functions.
	if len(pkg.Functions) > 0 {
		sb.WriteString("## Functions\n\n")
		for _, fn := range pkg.Functions {
			sb.WriteString(fmt.Sprintf("- `%s`\n", fn.Signature))
		}
		sb.WriteString("\n")
	}

	// Constants.
	if len(pkg.Constants) > 0 {
		sb.WriteString("## Constants\n\n")
		for _, c := range pkg.Constants {
			sb.WriteString(fmt.Sprintf("- `%s`\n", c))
		}
		sb.WriteString("\n")
	}

	// Change Impact.
	if impact != nil {
		hasImpact := len(impact.AdapterFiles) > 0 || len(impact.Mocks) > 0 || len(impact.TypeRefs) > 0
		if hasImpact {
			sb.WriteString("## Change Impact\n\n")

			if len(impact.AdapterFiles) > 0 {
				sb.WriteString("**Adapters (update when interfaces change):**\n")
				for _, f := range impact.AdapterFiles {
					sb.WriteString(fmt.Sprintf("- `%s`\n", f))
				}
				sb.WriteString("\n")
			}

			if len(impact.Mocks) > 0 {
				sb.WriteString("**Test Mocks (must add new methods when interfaces change):**\n")
				for _, m := range impact.Mocks {
					sb.WriteString(fmt.Sprintf("- `%s` in `%s`\n", m.TypeName, m.File))
				}
				sb.WriteString("\n")
			}

			// Show type references grouped by type name.
			if len(impact.TypeRefs) > 0 {
				sb.WriteString("**Exported type references (files that use types from this package):**\n")
				// Sort type names.
				var typeNames []string
				for name := range impact.TypeRefs {
					typeNames = append(typeNames, name)
				}
				sort.Strings(typeNames)

				for _, name := range typeNames {
					refs := impact.TypeRefs[name]
					if len(refs) == 0 {
						continue
					}
					sb.WriteString(fmt.Sprintf("- `%s` → ", name))
					var files []string
					for _, ref := range refs {
						tag := ""
						if ref.IsTest {
							tag = " (test)"
						}
						files = append(files, "`"+ref.File+"`"+tag)
					}
					if len(files) > 5 {
						sb.WriteString(strings.Join(files[:5], ", "))
						sb.WriteString(fmt.Sprintf(" + %d more", len(files)-5))
					} else {
						sb.WriteString(strings.Join(files, ", "))
					}
					sb.WriteString("\n")
				}
				sb.WriteString("\n")
			}
		}
	}

	// Dependencies.
	sb.WriteString("## Dependencies\n\n")
	forward := deps.Forward[pkg.ImportPath]
	reverse := deps.Reverse[pkg.ImportPath]
	if len(forward) > 0 {
		sb.WriteString("**Imports:** ")
		sb.WriteString(formatDepList(forward))
		sb.WriteString("\n\n")
	} else {
		sb.WriteString("**Imports:** *(none — zero-dependency)*\n\n")
	}
	if len(reverse) > 0 {
		sb.WriteString("**Imported by:** ")
		sb.WriteString(formatDepList(reverse))
		sb.WriteString("\n\n")
	}

	// Design Notes separator — everything below this line is preserved across regeneration.
	sb.WriteString("<!-- AUTO-GENERATED ABOVE — DO NOT EDIT -->\n")
	sb.WriteString("<!-- MANUAL NOTES BELOW — preserved across regeneration -->\n")

	return sb.String()
}

// ── Helpers ──────────────────────────────────────────────────────────────────

func summarizeExports(pkg *PackageInfo) string {
	var parts []string
	for _, iface := range pkg.Interfaces {
		parts = append(parts, fmt.Sprintf("%s(%dm)", iface.Name, len(iface.Methods)))
	}
	for _, st := range pkg.Structs {
		if len(parts) < 5 {
			parts = append(parts, st.Name)
		}
	}
	if len(parts) > 5 {
		parts = parts[:5]
		parts = append(parts, "...")
	}
	if len(parts) == 0 {
		return "-"
	}
	return strings.Join(parts, ", ")
}

func formatDepList(deps []string) string {
	short := make([]string, len(deps))
	for i, d := range deps {
		short[i] = "`" + d + "`"
	}
	return strings.Join(short, ", ")
}

func countLayers(byLayer map[string][]*PackageInfo) int {
	count := 0
	for _, pkgs := range byLayer {
		if len(pkgs) > 0 {
			count++
		}
	}
	return count
}

// designNotesSeparator is the marker line that separates auto-generated content
// from manually written Design Notes. Everything after this marker is preserved
// across regeneration.
const designNotesSeparator = "<!-- MANUAL NOTES BELOW — preserved across regeneration -->"

// extractDesignNotes reads an existing CONTEXT.md file and returns any content
// after the designNotesSeparator. Returns "" if the file doesn't exist or has
// no manual notes.
func extractDesignNotes(path string) string {
	data, err := os.ReadFile(path)
	if err != nil {
		return ""
	}
	content := string(data)
	idx := strings.Index(content, designNotesSeparator)
	if idx < 0 {
		return ""
	}
	notes := strings.TrimSpace(content[idx+len(designNotesSeparator):])
	if notes == "" {
		return ""
	}
	return notes
}

// appendDesignNotes appends preserved Design Notes to a newly generated
// CONTEXT.md content. If notes is empty, returns content unchanged.
func appendDesignNotes(content, notes string) string {
	if notes == "" {
		return content
	}
	return content + "\n" + notes + "\n"
}
